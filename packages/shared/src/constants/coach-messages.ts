export function buildDefaultPushTemplates(): Array<{
  eventType: PushEventType;
  harshnessLevel: number;
  message: string;
}> {
  return PUSH_EVENT_TYPES.flatMap((eventType) =>
    HARSHNESS_LEVELS.map((harshnessLevel) => ({
      eventType,
      harshnessLevel,
      message: PUSH_COACH_MESSAGES[eventType][harshnessLevel],
    })),
  );
}
